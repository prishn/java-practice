package datatype;

public class Trial {
    public static void main(String [] args) {
        byte a = 125;
        short b = 32000;
        int d = 799987876;
        long c = 2^5;
        System.out.println(a+b);
        System.out.println(c+d);
        System.out.println((a+b)!=(c+d));
        System.out.print("a+b = ");
        System.out.println(c+d);
        byte e = 100;
        System.out.println(e);
    }
}
