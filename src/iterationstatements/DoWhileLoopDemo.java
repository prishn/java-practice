package iterationstatements;

public class DoWhileLoopDemo {

    public static void main(String[] args) {
        int i = 1;
        do{
            System.out.println(i);
            i++;
        }while(i<=5);
        //print number between 10 to 20
        int j = 10;
        do{
            System.out.println(j);
            j++;
        }while(j<=20);

    }
}
